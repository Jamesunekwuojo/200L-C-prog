#include <stdio.h>
int main (){

	int x =10, y=2;
    // pre- increament value
    printf("Value of x is %d \n", x);
    printf("value of x is %d  \n", ++x);
    printf("value of x increament is %d \n", x);
    
    // post increament
    printf("original y is %d \n", y);
    printf("value of x is %d \n", y++);
    printf("Value of increamented y is %d", y);

	
	
}